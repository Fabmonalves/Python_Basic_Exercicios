a = input('Digite algo: ')
print('O tipo primitivo desse valor é {}'.format(type(a)))
print('Só tem espaços? {}!'.format(a.isspace()))
print('É um numero? {}!'.format(a.isnumeric()))
print('É alfabetico? {}!'.format(a.isalpha()))
print('É alfanumeroco? {}!'.format(a.isalnum()))
print('Está em maiúscula? {}!'.format(a.isupper()))
print('Está em minúscula? {}!'.format(a.islower()))
print('Está capitalizada? {}!'.format(a.istitle()))

#esses comandos dizem o tipo do conteudo que for inserido
